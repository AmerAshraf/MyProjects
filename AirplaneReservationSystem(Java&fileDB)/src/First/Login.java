    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(txtUserName.getText().equals("")&&txtPassWord.getText().equals("")){JOptionPane.showMessageDialog(null, "Missing UserName and Password !", " Login ERROR ", 1);}
        else{  Adminstration x =new Adminstration();
       int y = x.Login(txtUserName.getText(), txtPassWord.getText());
        if (y==2)
       {
                Loader.show();
                Log.hide();
        
        // timer
        
        new java.util.Timer().schedule(new TimerTask() {
            @Override
            public void run() {
                new AdminMiddle().show();
                dispose();
            }
        },1000*5);
      
       }
       else if (y==1)
       {
                Loader.show();
                Log.hide();
        
        // timer
        
        new java.util.Timer().schedule(new TimerTask() {
            @Override
            public void run() {
                new EmployeeMiddle().show();
                dispose();
            }
        },1000*5);
        }
       else 
       {
           JOptionPane.showMessageDialog(null, "Wrong UserName or Password !", " Login ERROR ", 1);
           resetU_PData();
           txtUserName.requestFocus();
       }
    
        }
               
   
    }//GEN-LAST:event_btnLoginActionPerformed

