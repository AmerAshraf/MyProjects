    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(SetProcesses(rr.NoOfProcesses())){
            rr.SortProcess();
            rr.Run_RR();
            
            avgw.setText(String.valueOf(rr.AVGWaitingTime()) + " " + "ms");
            avgt.setText(String.valueOf(rr.AVGTurnAroundTime()) + " " + "ms");
            avgr.setText(String.valueOf(rr.AVGResponsedTime())  + " " + "ms");
            
            gantchart();

            P2.show();
            P3.hide();

            // timer

            new java.util.Timer().schedule(new TimerTask() {
                @Override
                public void run() {
                    P2.hide();
                    P4.show();
                }
            },1000*1);}
        else{
            //JOptionPane.showMessageDialog(null, "not allowed set Arrival time or Burst time empty or string");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

