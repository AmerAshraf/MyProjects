                ins.setPreferredSize(new Dimension(10,10));
                smallpanal.get(i).add(ins);
            smallpanal.get(i).add(name.get(i));
                JLabel outs = new JLabel(String.valueOf(out));
                outs.setFont(new Font("Serif", Font.BOLD, 10));
                outs.setForeground(Color.white);
                outs.setPreferredSize(new Dimension(10,10));
                smallpanal.get(i).add(outs);
