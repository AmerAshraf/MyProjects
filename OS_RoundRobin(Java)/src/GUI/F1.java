            for(int ii=0;ii<rr.Processes.size();ii++){
                if(rr.WaitingQueue.get(i).getName() .equals(rr.Processes.get(ii).getName())){
                    in = rr.WaitingQueue.get(i).getIn().get(arr[ii]);
                    out = rr.WaitingQueue.get(i).getOut().get(arr[ii]);
                    arr[ii]++;
                    size=((out-in)*7)+80;
                }
            }
            //-------------------------------
            smallpanal.get(i).setPreferredSize(new Dimension(size,30));
